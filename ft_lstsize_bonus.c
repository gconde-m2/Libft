#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int cont;

	cont = 0;
	if (lst)
	{
		while (lst->next)
		{
			lst = lst->next;
			cont++;
		}
		cont++;
	}
	return (cont);
}
